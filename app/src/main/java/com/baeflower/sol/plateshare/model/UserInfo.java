public class UserInfo {

    private long mId;
    private String mUniv;
    private String mLocation;
    private String mMapLocation;

    public long getmId() {
        return mId;
    }

    public void setmId(long mId) {
        this.mId = mId;
    }

    public String getmUniv() {
        return mUniv;
    }

    public void setmUniv(String mUniv) {
        this.mUniv = mUniv;
    }

    public String getmLocation() {
        return mLocation;
    }

    public void setmLocation(String mLocation) {
        this.mLocation = mLocation;
    }

    public String getmMapLocation() {
        return mMapLocation;
    }

    public void setmMapLocation(String mMapLocation) {
        this.mMapLocation = mMapLocation;
    }
}
